    private void boxtipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxtipoActionPerformed
        //Guardar las variables del txtfield y el combo box
        String tipo = boxtipo.getSelectedItem().toString();
        String codigo = txtcod.getText();
        
        //Comprobar si lo que contiene el txtfield no está vacío
        if(codigo.length() != 0) {
            if(tipo.charAt(0) == codigo.charAt(0)) {
                return;
            }
        }
        
        //Añadir un caracter al inicio de mi string codigo
        switch(tipo) {
            case "Factura" -> codigo = "F";
            case "Boleta" -> codigo = "B";
        }
        
        //guardar y presentar
        codigo += generarCodigo();
        txtcod.setText(codigo);
    }//GEN-LAST:event_boxtipoActionPerformed

