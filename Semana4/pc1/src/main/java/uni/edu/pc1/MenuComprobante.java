    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        //Comprobación que los datos de la fecha sean números y cumplan con la sintáxis de la fecha
        String dia = txtdia.getText();
        String mes = txtmes.getText();
        String anio = txtanio.getText();
        String fecha = "";
        if(!anio.isEmpty() && !mes.isEmpty() && !dia.isEmpty()) {
            if(!isNumeric(anio) || anio.length() != 4) {
                System.out.println("Tienes que ingresar un anio valido (4 digitos)");
                return;
            }
            if(!isNumeric(mes) || Integer.parseInt(mes) > 12 || Integer.parseInt(mes) <= 0) {
                System.out.println("Tienes que ingresar un mes valido (1 - 12)");
                return;
            }
            if(!isNumeric(dia) || Integer.parseInt(dia) > 30 || Integer.parseInt(dia) <= 0) {
                System.out.println("Tienes que ingresar un dia valido (1 - 30)");
                return;
            }
            fecha = dia + "/" + mes + "/" + anio;
        }
        
        
        //Declaración y definición de variables
        String nombre = txtname.getText();
        String seldoc = String.valueOf(boxdoc.getSelectedItem());
        String doc = txtdoc.getText();
        String tipo = String.valueOf(boxtipo.getSelectedItem());
        String codigo = txtcod.getText();
        
        //excepciones
        if(nombre.isEmpty()) {
            return;
        }
        switch(seldoc) {
            case "DNI" -> {
                if(!isNumeric(doc) || doc.length() != 8  || tipo.equals("Factura")) {
                    System.out.println("No se han ingresado los datos correctos, DNI tiene que ir con Boleta y tiene que tener 8 digitos");
                    return;
                }
            }
            case "RUC" -> {
                if(!isNumeric(doc) || doc.length() != 11 || tipo.equals("Boleta")) {
                    System.out.println("No se han ingresado los datos correctos, RUC tiene que ir con Factura y tiene que tener 11 digitos");
                    return;
                }
            }
        }
        
        //Declaración y definición de objetos
        Cliente cliente = new Cliente(nombre, seldoc, doc);
        Comprobante comprobante;
        if(fecha.equals("")) {
            if(tipo.equals("Boleta")) {
                comprobante = new Boleta(cliente, codigo);
            }   else {
                comprobante = new Factura(cliente, codigo);
            }
        }   else {
            if(tipo.equals("Boleta")) {
                comprobante = new Boleta(cliente, fecha, codigo);
            }   else{
                comprobante = new Factura(cliente, fecha, codigo);
            }
            
        }
        for(int fila = 0; fila < table.getRowCount(); fila++) {
            //Obtengo el valor de cada elemento de la tabla, para agregar a mi producto
            String codigoprod = table.getValueAt(fila, 0).toString();
            String nombreprod = table.getValueAt(fila, 1).toString();
            double preciounitprod = Double.parseDouble(table.getValueAt(fila, 2).toString());
            int cantidadprod = Integer.parseInt(table.getValueAt(fila, 4).toString());
            String descuentoprod = table.getValueAt(fila, 5).toString();
            descuentoprod = descuentoprod.replace("%", "");
            
            Producto producto;
            if (descuentoprod.equals("No aplica")) {
                producto = new ProductoNormal(nombreprod, preciounitprod, cantidadprod, codigoprod);
            } else {
                producto = new ProductoConDescuento(nombreprod, preciounitprod, cantidadprod, codigoprod, Double.parseDouble(descuentoprod));
            }
            comprobante.AgregarProducto(producto);
        }
        
        //Visualización del comprobante de pago
        System.out.println(comprobante.GenerarComprobante());
        
        
    }//GEN-LAST:event_btnActionPerformed

