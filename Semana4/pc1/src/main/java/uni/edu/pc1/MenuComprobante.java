    private void btngenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngenerateActionPerformed
        //Variables que guardan la información que están en los txtfield
        String codigo = txtcodigoprod.getText();
        String nombre = txtnombre.getText();
        String punit = txtunit.getText();
        String cantidad = txtcantidad.getText();
        
        //Variables numéricas para el uso de operaciones
        Double nunit;
        Double nprecio;
        int ncantidad;
        Double nimporte;

        //Comprobaciones
        if(!isNumeric(codigo) || codigo.length() < 0) {
            System.out.println("Tienes que ingresar un codigo valido (tipo numerico)");
            return;
        }
        if(!isNumeric(punit) || Double.parseDouble(punit) < 0) {
            System.out.println("Tienes que ingrear un precio unitario valido");
            return;
        }
        if(!isNumeric(cantidad) || Integer.parseInt(cantidad) < 0) {
            System.out.println("Tienes que ingrear una cantidad valida");
            return;
        }
        
        //Definición de las variables
        nunit = Double.parseDouble(punit);
        ncantidad = Integer.parseInt(cantidad);
        nprecio = (double) Math.round(nunit * 118) / 100;
        String descuento = "No aplica";
        Double ndescuento;
        
        //Comprobación para productos con descuento
        if(numdesc.isEnabled()) {
            descuento = numdesc.getValue().toString();
            ndescuento = Double.parseDouble(descuento);
            if(ndescuento < 0 || ndescuento > 100) {
                return;
            }
            descuento += "%";
            ndescuento *= nprecio * ncantidad;
            ndescuento = (double) Math.round(ndescuento) / 100;
            nimporte = nprecio * ncantidad - ndescuento;
        }   else{
            nimporte = nprecio * ncantidad;
        }
        
        //convertir en string los valores hallados, para guardarlos en el array
        String precio = String.valueOf(nprecio);
        String importe = String.valueOf(nimporte);
        String[] arraycodigo = {codigo, nombre, punit, precio, cantidad, descuento, importe};
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.addRow(arraycodigo);
    }//GEN-LAST:event_btngenerateActionPerformed

